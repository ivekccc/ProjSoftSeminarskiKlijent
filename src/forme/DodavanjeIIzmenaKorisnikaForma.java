    private void jButtonDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDodajActionPerformed
        String ime=jTextFieldIme.getText();
        String prezime=jTextFieldPrezime.getText();
        String email=jTextFieldEmail.getText();
        String brojTelefona=jTextFieldBrojTelefona.getText();
        if(ime.isEmpty() || prezime.isEmpty() || email.isEmpty() || brojTelefona.isEmpty()){
            JOptionPane.showMessageDialog(this, "Neuspesno dodavanje korisnika. Sva polja moraju da budu popunjena","Upozorenje",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Neuspesno dodavanje korisnika. Email mora sadr≈æati '@' simbol.", "Upozorenje", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Korisnik k=new Korisnik(-1, ime, prezime, email, brojTelefona, 0,gf.getUlogovaniAdmin());
        Response response=UserInterfaceController.getInstance().dodajKorisnika(k);
        if(response.isSuccessful()){
            JOptionPane.showMessageDialog(this, response.getMessage(),"Uspeh",JOptionPane.INFORMATION_MESSAGE);
            gf.popuniListuKorisnika();
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, response.getMessage(),"Greska",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonDodajActionPerformed

    private void jButtonIzmenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIzmenaActionPerformed
        String ime=jTextFieldIme.getText();
        String prezime=jTextFieldPrezime.getText();
        String email=jTextFieldEmail.getText();
        String brojTelefona=jTextFieldBrojTelefona.getText();
        if(ime.isEmpty() || prezime.isEmpty() || email.isEmpty() || brojTelefona.isEmpty() ){
            JOptionPane.showMessageDialog(this, "Neuspesna izmena korisnika. Sva polja moraju da budu popunjena","Upozorenje",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Neuspesna izmena korisnika. Email mora sadr≈æati '@' simbol.", "Upozorenje", JOptionPane.WARNING_MESSAGE);
            return;
        }
        korisnikZaIzmenu.setIme(ime);
        korisnikZaIzmenu.setPrezime(prezime);
        korisnikZaIzmenu.setEmail(email);
        korisnikZaIzmenu.setBrojTelefona(brojTelefona);
        Response response=UserInterfaceController.getInstance().izmenaKorisnika(korisnikZaIzmenu);
        if(response.isSuccessful()){
            JOptionPane.showMessageDialog(this, response.getMessage(),"Uspeh",JOptionPane.INFORMATION_MESSAGE);
            gf.popuniListuKorisnika();
            this.dispose();
            return;
        }
        else{
            JOptionPane.showMessageDialog(this, response.getMessage(),"Greska",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButtonIzmenaActionPerformed

